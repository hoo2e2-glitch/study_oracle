-- 주소가 '구'로 끝나는 유저가 작성한 게시글에 달린 모든 댓글 조회

SELECT *
FROM TBL_REPLY TBR
JOIN (
	SELECT POST_CONTENT 
	FROM TBL_POST
	WHERE ID IN (
		SELECT USER_ID  
		FROM TBL_USER
		WHERE USER_ADDRESS LIKE '%구%'
		)
	) TBP
ON TBR.ID = TBP.POST_ID;


-- 평균 댓글 개수보다 작게 달린 게시글을 작성한 유저 조회
-- 유저 조회

SELECT * 
FROM TBL_POST TBP
JOIN (
	SELECT * 
	FROM TBL_USER
	WHERE ID IN (
			-- 작은댓글
			SELECT MIN(POST_ID) 
			FROM (
				SELECT COUNT(POST_ID) "POST_ID" 
				FROM TBL_REPLY
				GROUP BY POST_ID
				
				)
			-- 평균댓글
			SELECT AVG(POST_COUNT) 
			FROM (
				-- 갯수
				SELECT COUNT(POST_ID) "POST_COUNT"
				FROM TBL_REPLY
				GROUP BY POST_ID 
				)
		)
) TBR
ON TBR.POST_ID = TBP.ID;













