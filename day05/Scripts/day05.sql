--댓글이 2개이상 달린 게시글의 작성자 이메일, 주소를 조회하기
SELECT * FROM TBL_POST;

SELECT *
FROM TBL_POST TBP
JOIN (
	SELECT POST_ID
	FROM TBL_REPLY
	GROUP BY POST_ID
	HAVING COUNT(POST_ID) >= 2
	) TBR
ON TBP.ID = TBR.POST_ID;

-- 같은풀이
SELECT DISTINCT TBU.USER_EMAIL, TBU.USER_ADDRESS
